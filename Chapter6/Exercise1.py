import math

# For a = 2.3 find the ceil of a
a = 2.3
ceil_a = math.ceil(a)
print("Ceil of", a, "is", ceil_a)

# For a = 2.3 find the floor of a
floor_a = math.floor(a)
print("Floor of", a, "is", floor_a)

# For a = 5 find the factorial of a
a = 5
factorial_a = math.factorial(a)
print("Factorial of", a, "is", factorial_a)

# Find the value of 23
e_23 = math.exp(23)
print("e to the power of 23 is", e_23)

# For a = 16 find the square root of a
a = 16
sqrt_a = math.sqrt(a)
print("Square root of", a, "is", sqrt_a)